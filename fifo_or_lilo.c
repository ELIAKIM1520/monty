#include "monty.h"

/**
  * _lifo - it changes the addition of the stack to LIFO
  * @stack: NULL
  * @line_number: still  0
  * Return: return nothing
  */
void _lifo(stack_t **stack, unsigned int line_number)
{
	(void) line_number;
	(void) stack;

	element->FO = 0;
}

/**
  * _fifo - it should change addition of the stack to FIFO
  * @stack: NULL
  * @line_number: still 0
  * Return: nothing, void
  */
void _fifo(stack_t **stack, unsigned int line_number)
{
	(void) stack;
	(void) line_number;

	element->FO = 1;
}
